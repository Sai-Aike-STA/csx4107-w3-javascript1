let x = 5
let y = 5


// Do ... while
do {
    console.log("do... While x = ", x);
    x ++;
} while (x < 10);

// While
while (y < 10) {
    console.log("while ... : y = ", y)
    y++
}

// For Loops
for (let i = 0; i < 10; i++) {
    console.log("i = ", i);
}

let exampleList = [2, 4, 6, 8]
for (let item of exampleList) {
    console.log(item);
}